<p align="center">INSTRUCTIONS TO CANDIDATE</p>
<p>Candidate should bring with them to the Examination Hall on each day of the Examination, their Hall Ticket and Identity
Card for inspection by Chief Superintendent.</p></br>
<p>Candidate are allowed to take only the Hall Ticket with them to the Examination Hall.
</p></br>
<p>Candidate are prohibited from writing on their Hall Tickets</p></br>
<p>In case the Hall Ticket is lost by the Candidate, they sholud report immediately the fact to the Chief Superintendent of the
Examination Centre, Admission of the Candidates may be permitted by the Chief Superintendent himself, with concurrence of the
Dean.
</p>
